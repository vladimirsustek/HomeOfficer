/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

	mouseHID mouseState = {0, 0, 0, 0};
	cursorRandState_t state = MoveForward;
	uint32_t lastTick = 0;
	uint32_t timeToWait = 0;

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */
  MAIN_ShortcutUSB();
  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_USB_DEVICE_Init();
  /* USER CODE BEGIN 2 */

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

	  if(HAL_GetTick() > lastTick + timeToWait)
	  {
		  /* Why MoveForward and MoveBack ? Because otherwise would cursor once end up in a corner.. */
		  switch(state)
		  {
		  case MoveForward :
		  {
			  /*Rand X by getting ADC noise VAL*/
			  HAL_ADC_Start(&hadc1);
			  mouseState.mouse_x = (int8_t)HAL_ADC_GetValue(&hadc1)/16;
			  HAL_ADC_Stop(&hadc1);

			  /*Rand Y by getting ADC noise VAL*/
			  HAL_ADC_Start(&hadc1);
			  mouseState.mouse_y = (int8_t)HAL_ADC_GetValue(&hadc1)/16;
			  HAL_ADC_Stop(&hadc1);

			  /* Send an report to PC*/
			  USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t*)&mouseState, sizeof(mouseState));

			  timeToWait = (mouseState.mouse_x & 0xF) * 1000;

			  state = MoveBack;

		  }
		  break;
		  case MoveBack :
		  {
			  /* Convert coordinates to move back */
			  mouseState.mouse_x = -1*mouseState.mouse_x;
			  mouseState.mouse_y = -1*mouseState.mouse_y;

			  /* Send an report to PC*/
			  USBD_HID_SendReport(&hUsbDeviceFS, (uint8_t*)&mouseState, sizeof(mouseState));

			  state = MoveForward;

		  }
		  default :
		  {
			  state = MoveForward;
		  }
		  }

		  lastTick = HAL_GetTick();

		  /* Signalizing that device works (LED flash)*/
		  HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_RESET);
		  HAL_Delay(200);
		  HAL_GPIO_WritePin(LED_GPIO_Port, LED_Pin, GPIO_PIN_SET);
	  }

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

